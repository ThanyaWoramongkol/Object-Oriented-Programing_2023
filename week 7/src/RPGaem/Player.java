package RPGaem;
public class Player {
    private double ATK;
    private double EXP;
    private double HP;
    private double MP;
    private double Money;
    
    public void setATK(double ATK){ this.ATK = ATK; }
    
    public double getATK(){ return this.ATK; }
    
    public void setEXP(double EXP){ this.EXP = EXP; }
    
    public double getEXP(){ return this.EXP; }
    
    public void setHP(double HP){ this.HP = HP; }
    
    public double getHP(){ return this.HP; }
    
    public void setMP(double MP){ this.MP = MP; }
    
    public double getMP(){ return this.MP; }
    
    public void setMoney(double Money){ this.EXP = Money; }
    
    public double getMoney(){ return this.Money; }
}
